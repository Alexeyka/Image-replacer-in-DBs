    public void updateImage() {
        try {
            BufferedImage current = c.im.getImage();
            BufferedImage result = Thumbnails.of(current)
                    .size(120, 120).asBufferedImage();
            ImageIcon s = new ImageIcon(result);
            jLabel3.setIcon(s);
        } catch (Exception e) {
            System.out.println("Cant convert icon");
        }
    }

