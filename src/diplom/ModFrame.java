    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try {
            File file = null;
            jFileChooser1.addChoosableFileFilter(new ImageFileFilter());
            int returnVal = jFileChooser1.showOpenDialog(null);

            if (returnVal == JFileChooser.APPROVE_OPTION) {
                file = jFileChooser1.getSelectedFile();
                if (file.exists()) {
                    JOptionPane.showMessageDialog(null, "Перезапись файлов не доступна");
                    return;
                }
                String path = file.getPath();
                BufferedImage img = c.im.getImage();
                Thumbnails.of(img)
                        .size(img.getWidth(), img.getHeight())
                        .toFile(new File(path));
                MainFrame.count++;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ошибка при записи файла");
        }

    }//GEN-LAST:event_jButton6ActionPerformed

